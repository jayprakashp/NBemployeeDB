    private void btnEmpUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEmpUpdateActionPerformed
       if(validateInput()==false){
           try{
          Employee e=EmployeeDAO.findEmployeeById(Integer.parseInt(txtEmpNo.getText()));
           if(e!=null){
               if(!txtEmpName.getText().isEmpty()){
                   e.setEname(txtEmpName.getText());
               }
               if(!txtSal.getText().isEmpty()){
                   e.setSal(Double.parseDouble(txtSal.getText()));
               }
               if(EmployeeDAO.updateEmployee(e)){
                   JOptionPane.showMessageDialog(null,"Employee Record Updated Successfully","Success",JOptionPane.INFORMATION_MESSAGE);
               }
               else
                   JOptionPane.showMessageDialog(null,"Updation Failed !","Error",JOptionPane.ERROR_MESSAGE);
           }
           else{
               JOptionPane.showMessageDialog(null,"Employee Record Not Found","Error",JOptionPane.ERROR_MESSAGE);
           }
         }catch(SQLException ex){
               JOptionPane.showMessageDialog(null,"SQL Error "+ex,"Error",JOptionPane.ERROR_MESSAGE);
              }
       }
       else
           JOptionPane.showMessageDialog(null,"Please input Emp No field","Error",JOptionPane.ERROR_MESSAGE);

    }//GEN-LAST:event_btnEmpUpdateActionPerformed

